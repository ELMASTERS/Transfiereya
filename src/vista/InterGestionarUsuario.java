    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed



if (txt_nombre.getText().isEmpty() || txt_apellido.getText().isEmpty()
            || txt_usuario.getText().isEmpty() || txt_telefono.getText().isEmpty() || jComboBox1_rolUsuario.getSelectedItem().equals("Seleccione un Rol")) {
        JOptionPane.showMessageDialog(null, "¡Completa todos los campos!");
    } else {
        Usuario usuario = new Usuario();
        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();

        usuario.setNombre(txt_nombre.getText().trim());
        usuario.setApellido(txt_apellido.getText().trim());
        usuario.setUsuario(txt_usuario.getText().trim());
        
        // Obtenemos la contraseña de acuerdo al campo visible
        String password;
        if (txt_password.isVisible()) {
            password = new String(txt_password.getPassword()).trim();
        } else {
            password = txt_password_visible.getText().trim();
        }
        usuario.setPassword(password);
        
        usuario.setTelefono(txt_telefono.getText().trim());
        usuario.setRolUsuario((String) jComboBox1_rolUsuario.getSelectedItem());

        if (controlUsuario.actualizar(usuario, idUsuario)) {
            JOptionPane.showMessageDialog(null, "¡Datos del Usuario actualizados!");
            this.CargarTablaUsuarios();
            this.Limpiar();
            idUsuario = 0;
        } else {
            JOptionPane.showMessageDialog(null, "¡Error al actualizar!");
            this.Limpiar();
        }
    }




    }//GEN-LAST:event_jButton_actualizarActionPerformed

    private void jButton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarActionPerformed

        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
        if (idUsuario == 0) {
            JOptionPane.showMessageDialog(null, "¡Seleccione un Usuario!");
        } else {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea eliminar este Usuario? el id# " + idUsuario, "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                if (!controlUsuario.eliminar(idUsuario)) {
                    JOptionPane.showMessageDialog(null, "¡Usuario Eliminado!");
                    this.CargarTablaUsuarios();
                    this.Limpiar();
                    idUsuario = 0;
                } else {
                    JOptionPane.showMessageDialog(null, "¡Error al eliminar Usuario!");
                    this.Limpiar();
                }
            }
        }

    }//GEN-LAST:event_jButton_eliminarActionPerformed

