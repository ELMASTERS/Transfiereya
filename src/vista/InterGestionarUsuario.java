    /*
     * *****************************************************
     * metodo para mostrar todos los Usuarios registrados
     * *****************************************************
     */
    private void CargarTablaUsuarios() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select * from tb_usuario";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarUsuario.jTable_usuarios = new JTable(model);
            InterGestionarUsuario.jScrollPane1.setViewportView(InterGestionarUsuario.jTable_usuarios);

            model.addColumn("N°");//ID
            model.addColumn("nombre");
            model.addColumn("apellido");
            model.addColumn("usuario");
            model.addColumn("password");

            model.addColumn("telefono");
            model.addColumn("rolUsuario");
            model.addColumn("estado");

            while (rs.next()) {
                Object fila[] = new Object[8];
                fila[0] = rs.getInt("idUsuario");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("apellido");
                fila[3] = rs.getString("usuario");
                // En lugar de la contraseña real, agregar asteriscos
                String password = rs.getString("password");
                StringBuilder asteriscos = new StringBuilder();
                for (int i = 0; i < password.length(); i++) {
                    asteriscos.append("*");
                }
                fila[4] = asteriscos.toString();
                fila[5] = rs.getString("telefono");
                fila[6] = rs.getString("rolUsuario");
                fila[7] = rs.getInt("estado");
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla Usuario: " + e);
        }
        //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        jTable_usuarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_usuarios.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idUsuario = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosUsuarioSeleccionado(idUsuario);//metodo
                }
            }
        });
    }


    /*
     * **************************************************
     * Metodo que envia datos seleccionados
     * **************************************************
     */
    private void EnviarDatosUsuarioSeleccionado(int idUsuario) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(
                    "select * from tb_usuario where idUsuario = '" + idUsuario + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellido.setText(rs.getString("apellido"));
                txt_usuario.setText(rs.getString("usuario"));
                txt_password.setText(rs.getString("password"));
                txt_password_visible.setText(rs.getString("password"));
                txt_telefono.setText(rs.getString("telefono"));
                String rolUsuario = rs.getString("rolUsuario");
                for (int i = 0; i < jComboBox1_rolUsuario.getItemCount(); i++) {
                    if (jComboBox1_rolUsuario.getItemAt(i).equals(rolUsuario)) {
                        jComboBox1_rolUsuario.setSelectedIndex(i);
                        break;
                    }
                }

            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar Usuario: " + e);
        }
    }

