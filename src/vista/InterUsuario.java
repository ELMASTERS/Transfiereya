    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed


        String rolSeleccionado = (String) jComboBox1_rolUsuario.getSelectedItem();
        Usuario usuario = new Usuario();
        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
        if (!txt_nombre.getText().isEmpty() && !txt_apellido.getText().isEmpty() && !txt_usuario.getText().isEmpty() && !rolSeleccionado.equals("Seleccione un Rol") ) {
            if (!controlUsuario.exiteUsuario(txt_usuario.getText().trim())) {
                usuario.setNombre(txt_nombre.getText().trim());
                usuario.setApellido(txt_apellido.getText().trim());
                usuario.setUsuario(txt_usuario.getText().trim());
                usuario.setPassword(new String(txt_password.getPassword()).trim());
                usuario.setTelefono(txt_telefono.getText().trim());
                usuario.setRolUsuario(jComboBox1_rolUsuario.getSelectedItem().toString());
                usuario.setEstado(1);
                if (controlUsuario.guardar(usuario)) {
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                    txt_nombre.setBackground(Color.green);
                    txt_apellido.setBackground(Color.green);
                    txt_usuario.setBackground(Color.green);
                    txt_password.setBackground(Color.green);
                    txt_telefono.setBackground(Color.green);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al Guardar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El Usuario ya est√° registrado", "Error", JOptionPane.ERROR_MESSAGE);
                txt_nombre.setBackground(Color.white);
                txt_apellido.setBackground(Color.white);
                txt_usuario.setBackground(Color.white);
                txt_password.setBackground(Color.white);
                txt_telefono.setBackground(Color.white);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Completa todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            txt_nombre.setBackground(Color.red);
            txt_apellido.setBackground(Color.red);
            txt_usuario.setBackground(Color.red);
            txt_password.setBackground(Color.red);
            txt_telefono.setBackground(Color.red);
        }
        this.Limpiar();


    }//GEN-LAST:event_jButton_guardarActionPerformed

